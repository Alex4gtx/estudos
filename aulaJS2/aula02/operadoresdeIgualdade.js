//operadores de igualdade
// igualdade estrita
// === | simbolo
// != | não é igual

console.log(1 === 1);
console.log('1' === 1); //diferencia valores e classes e os compara em sua forma / ate os tipos primitivos dela


//igualdade solta
console.log(1 == 1);
console.log('2' == 2); // esse tipo verifica apenas a igualdade dos valores, nesse caso vai ser convertido caso esteja em um tipo primitivo diferente
