let max = Math.max(1,4,5,3,7,9,8,10); //retorna o maior numero de uma lista/array
console.log(max);

let min = Math.min(3,4,3,6,7,8,599); //retorna o menor numero de um lista
console.log(min);

let pi = Math.PI; //retorna valor de pi
console.log(pi);

let abs = Math.abs(-5.60909); //mostra o numero absoluto, tira o numero negativo
console.log(abs);

let ceil = Math.ceil(7.1); //arredonda o numero flutuante para cima 5.1 = 6
console.log(ceil);

let floor = Math.floor(6.17656); //retira os numeros após o ponto
console.log(floor);

let potencia = Math.pow(2,3); // potência, 2 na 3, 2^3
console.log(potencia);

let random = Math.random(0,1); //numero qualquer entre 0 e 1
console.log(random);

let raizQuadrada = Math.sqrt(4); // raiz quadrada
console.log(raizQuadrada);
